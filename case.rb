print "Enter your day: "
day = gets.chomp
case day
when "Tuesday"
    puts "It is tuesday"
when "Wednesday"
    puts "It is wednesday"
when "Thursday" 
    puts "It is thursday"   
when "Friday"
    puts "It is friday"
when "Saturday"
    puts "It is saturday"
else
    puts "Day is not in the list"
end